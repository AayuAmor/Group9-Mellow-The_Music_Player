    /**
     * Load songs from the playlist and render in table
     */
    private void loadPlaylistSongs() {
        if (playlistId <= 0) {
            logger.warning("Invalid playlist ID: " + playlistId);
            return;
        }

        // Set playlist name
        playlistNameJLabel.setText(playlistName);

        // Fetch songs from database
        playlistSongs = playlistDao.getPlaylistSongs(playlistId);

        if (playlistSongs == null || playlistSongs.isEmpty()) {
            logger.info("No songs in playlist: " + playlistName);
            return;
        }

        // Render songs in table
        renderSongsTable();

        logger.info("Loaded " + playlistSongs.size() + " songs from playlist: " + playlistName);
    }

    /**
     * Render songs in the PlaylistTable
     * Columns: Song Name, Artist, Duration
     */
    private void renderSongsTable() {
        DefaultTableModel model = new DefaultTableModel(
                new String[] { "Song Name", "Artist", "Duration" }, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Make table read-only
            }
        };

        for (Song song : playlistSongs) {
            String duration = formatDuration(song.getDurationSeconds());
            model.addRow(new Object[] {
                    song.getTitle(),
                    song.getArtist(),
                    duration
            });
        }

        PlaylistTable.setModel(model);
        PlaylistTable.setFillsViewportHeight(true);

        // Set column widths
        if (PlaylistTable.getColumnModel().getColumnCount() > 0) {
            PlaylistTable.getColumnModel().getColumn(0).setPreferredWidth(250); // Song Name
            PlaylistTable.getColumnModel().getColumn(1).setPreferredWidth(200); // Artist
            PlaylistTable.getColumnModel().getColumn(2).setPreferredWidth(100); // Duration
        }

        // Add click listener to play song
        PlaylistTable.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = PlaylistTable.rowAtPoint(evt.getPoint());
                if (row >= 0 && row < playlistSongs.size()) {
                    Song selectedSong = playlistSongs.get(row);

                    // Send full playlist and selected index to PlaybackManager
                    PlaybackManager.getInstance().setPlaylist(playlistSongs, row, PlaySource.PLAYLIST);

                    // Open Player UI with correct play source
                    Player playerWindow = Player.getInstance();
                    playerWindow.setPlaySource(PlaySource.PLAYLIST);
                    playerWindow.setVisible(true);

                    logger.info("Playing from Playlist '" + playlistName + "': " + selectedSong.getTitle());
                }
            }
        });
    }

    /**
     * Format duration from seconds to MM:SS
     */
    private String formatDuration(int seconds) {
        int minutes = seconds / 60;
        int secs = seconds % 60;
        return String.format("%d:%02d", minutes, secs);
    }
