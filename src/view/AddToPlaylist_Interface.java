    /**
     * Setup playlist grid with PlaylistCard components in ADD mode
     */
    private void setupPlaylistGrid() {
        // Get playlists from cache (do NOT refetch)
        List<PlaylistModel> playlists = PlaylistManager.getInstance().getPlaylists();
        renderPlaylists(playlists);
    }

    /**
     * Render playlists in grid - ONLY clears and adds cards
     * Does NOT recreate panels or scroll panes
     */
    private void renderPlaylists(List<PlaylistModel> playlists) {
        // MUST clear old cards
        playlistGridPanel.removeAll();

        // Create PlaylistCard for each playlist in ADD mode
        for (PlaylistModel playlist : playlists) {
            PlaylistCard card = new PlaylistCard(
                    playlist,
                    PlaylistCard.Mode.ADD,
                    () -> handlePlaylistSelection(playlist));
            playlistGridPanel.add(card);
        }

        // REQUIRED: revalidate and repaint
        playlistGridPanel.revalidate();
        playlistGridPanel.repaint();

        logger.info("Rendered " + playlists.size() + " playlists in ADD mode");
    }

    /**
     * Handle playlist selection - show confirmation dialog and add song
     */
    private void handlePlaylistSelection(PlaylistModel playlist) {
        int result = JOptionPane.showConfirmDialog(
                this,
                "Do you want to add this song to \"" + playlist.getPlaylistName() + "\"?",
                "Add to Playlist",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (result == JOptionPane.YES_OPTION) {
            addSongToPlaylist(playlist);
        }
    }

    /**
     * Add song to playlist with FK integrity checks
     */
    private void addSongToPlaylist(PlaylistModel playlist) {
        try {
            // Step 1: Ensure song exists in database
            int songId = ensureSongInDatabase();
            if (songId <= 0) {
                JOptionPane.showMessageDialog(
                        this,
                        "Failed to register song in database",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Step 2: Insert into playlist_songs
            boolean success = playlistSongDao.addSongToPlaylist(
                    playlist.getPlaylistId(),
                    songId);

            if (success) {
                // Refresh cache
                PlaylistManager.getInstance().refreshCache();

                JOptionPane.showMessageDialog(
                        this,
                        "Song added to \"" + playlist.getPlaylistName() + "\" successfully!",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);

                logger.info("Added song " + songId + " to playlist " + playlist.getPlaylistId());

                // Close this dialog and return to Player
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "Song is already in this playlist",
                        "Info",
                        JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception e) {
            logger.severe("Error adding song to playlist: " + e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(
                    this,
                    "Failed to add song: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Ensure song exists in database
     * For filesystem songs: check by file_path, insert if missing
     * For DB songs: use existing song_id
     */
    private int ensureSongInDatabase() {
        if (currentSong == null) {
            logger.warning("Current song is null");
            return -1;
        }

        // If song already has ID from DB, use it
        if (currentSong.getSongId() > 0) {
            logger.info("Song already has valid ID: " + currentSong.getSongId());
            return currentSong.getSongId();
        }

        // For filesystem songs, check by file_path
        if (currentSong.getFilePath() != null && !currentSong.getFilePath().isEmpty()) {
            logger.info("Checking if song exists by file_path");
            int existingId = songDao.getSongIdByFilePath(currentSong.getFilePath());
            if (existingId > 0) {
                logger.info("Found existing song: ID = " + existingId);
                return existingId;
            }

            // Insert new song
            logger.info("Inserting new song: " + currentSong.getTitle());
            int newId = songDao.insertSongIfNotExists(currentSong);
            if (newId > 0) {
                logger.info("Inserted song with ID: " + newId);
                return newId;
            }
        }

        logger.warning("Could not register song in database");
        return -1;
    }
