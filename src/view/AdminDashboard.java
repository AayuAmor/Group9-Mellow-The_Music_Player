    private void loadUsers() {
        try {
            cachedUsers = userDao.getAllUsers();
            refreshUserTable();
            logger.info("Loaded users: " + cachedUsers.size());
        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, "Failed to load users", ex);
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Unable to load users. Please try again.",
                    "Load Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private void refreshUserTable() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new Object[] { "ID", "USERNAME", "EMAIL", "DATE CREATED", "ROLE" }, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (Model.UserData u : cachedUsers) {
            String created = u.getCreatedAt() != null ? u.getCreatedAt().toString() : "";
            model.addRow(new Object[] { u.getId(), u.getUsername(), u.getEmail(), created, u.getRole() });
        }

        UserTable.setModel(model);
        // Hide ID column from view but keep it for actions
        UserTable.getColumnModel().getColumn(0).setMinWidth(0);
        UserTable.getColumnModel().getColumn(0).setMaxWidth(0);
        UserTable.getColumnModel().getColumn(0).setWidth(0);
    }

    private void deleteSelectedUsers() {
        int[] selectedRows = UserTable.getSelectedRows();
        if (selectedRows.length == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select at least one user to delete.");
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Delete selected user(s)? This cannot be undone.",
                "Confirm Delete",
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.WARNING_MESSAGE);

        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) UserTable.getModel();
        java.util.List<Integer> failedIds = new java.util.ArrayList<>();

        for (int row : selectedRows) {
            Object idVal = model.getValueAt(row, 0);
            if (!(idVal instanceof Integer)) {
                continue;
            }
            int userId = (Integer) idVal;
            boolean success = userDao.deleteUserById(userId);
            if (!success) {
                failedIds.add(userId);
            }
        }

        loadUsers();

        if (!failedIds.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Failed to delete users: " + failedIds,
                    "Delete Failed",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private void showSelectedUser() {
        int row = UserTable.getSelectedRow();
        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a user to view.");
            return;
        }

        Object idVal = UserTable.getValueAt(row, 0);
        Object username = UserTable.getValueAt(row, 1);
        Object email = UserTable.getValueAt(row, 2);
        Object created = UserTable.getValueAt(row, 3);
        Object role = UserTable.getValueAt(row, 4);

        String message = "ID: " + idVal + "\n"
                + "Username: " + username + "\n"
                + "Email: " + email + "\n"
                + "Created: " + created + "\n"
                + "Role: " + role;

        javax.swing.JOptionPane.showMessageDialog(this, message, "User Details",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }

