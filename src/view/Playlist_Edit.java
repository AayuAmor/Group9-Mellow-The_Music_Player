    /**
     * Load songs from playlist using PlaylistSongDao
     */
    private void loadPlaylistSongs() {
        playlistSongs = playlistSongDao.getSongsByPlaylistId(playlist.getPlaylistId());

        DefaultTableModel model = new DefaultTableModel(
                new String[] { "ID", "Title", "Artist", "Album", "Duration" }, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (Song song : playlistSongs) {
            model.addRow(new Object[] {
                    song.getSongId(),
                    song.getTitle(),
                    song.getArtist(),
                    song.getAlbum(),
                    formatDuration(song.getDurationSeconds())
            });
        }

        PlaylistTable.setModel(model);
        PlaylistTable.getColumnModel().getColumn(0).setPreferredWidth(50);
        PlaylistTable.getColumnModel().getColumn(1).setPreferredWidth(200);
        PlaylistTable.getColumnModel().getColumn(2).setPreferredWidth(150);
        PlaylistTable.getColumnModel().getColumn(3).setPreferredWidth(150);
        PlaylistTable.getColumnModel().getColumn(4).setPreferredWidth(80);

        logger.info("Loaded " + playlistSongs.size() + " songs for playlist: " + playlist.getPlaylistName());
    }

    /**
     * Delete selected song from playlist
     */
    private void deleteSong() {
        int selectedRow = PlaylistTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this,
                    "Please select a song to delete",
                    "No Selection",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        int songId = (int) PlaylistTable.getValueAt(selectedRow, 0);
        String songTitle = (String) PlaylistTable.getValueAt(selectedRow, 1);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to remove \"" + songTitle + "\" from this playlist?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            boolean success = playlistSongDao.removeSongFromPlaylist(playlist.getPlaylistId(), songId);
            if (success) {
                JOptionPane.showMessageDialog(this,
                        "Song removed successfully",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);
                loadPlaylistSongs(); // Refresh table
            } else {
                JOptionPane.showMessageDialog(this,
                        "Failed to remove song",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    /**
     * Save playlist name changes
     */
    private void saveChanges() {
        String newName = playlistNameTextField.getText().trim();

        if (newName.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Playlist name cannot be empty",
                    "Invalid Name",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        boolean success = playlistDao.updatePlaylistName(playlist.getPlaylistId(), newName);

        if (success) {
            JOptionPane.showMessageDialog(this,
                    "Playlist updated successfully",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);

            // Refresh PlaylistManager cache
            PlaylistManager.getInstance().refreshCache();

            // Go back to Playlist view
            goBack();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Failed to update playlist",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Go back to Playlist view
     */
    private void goBack() {
        Playlist playlistView = new Playlist();
        playlistView.setVisible(true);
        this.dispose();
    }

    /**
     * Format duration from seconds to MM:SS
     */
    private String formatDuration(int seconds) {
        int minutes = seconds / 60;
        int secs = seconds % 60;
        return String.format("%d:%02d", minutes, secs);
    }

