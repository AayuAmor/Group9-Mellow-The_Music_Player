    private void CreatenewActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_CreatenewActionPerformed
        // Prompt user for playlist name
        String playlistName = JOptionPane.showInputDialog(this, "Enter playlist name:", "Create New Playlist",
                JOptionPane.PLAIN_MESSAGE);

        if (playlistName != null && !playlistName.trim().isEmpty()) {
            UserSession session = UserSession.getInstance();
            if (session.isLoggedIn()) {
                int userId = session.getUserId();
                int newPlaylistId = playlistDao.createPlaylist(userId, playlistName.trim());

                if (newPlaylistId > 0) {
                    logger.info("Created playlist: " + playlistName + " (ID: " + newPlaylistId + ")");
                    JOptionPane.showMessageDialog(this, "Playlist created successfully!", "Success",
                            JOptionPane.INFORMATION_MESSAGE);

                    // Refresh cache and re-render cards
                    PlaylistManager.getInstance().refreshCache();
                    List<PlaylistModel> playlists = PlaylistManager.getInstance().getPlaylists();
                    renderPlaylists(playlists);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to create playlist", "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "You must be logged in to create playlists", "Error",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
    }// GEN-LAST:event_CreatenewActionPerformed

